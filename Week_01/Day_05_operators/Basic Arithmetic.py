# taking user input
num1 = int(input("Enter 1st number: "))
num2 = int(input("Enter 2nd number: "))
# sum
sum = num1 + num2
print(f"Sum: {sum}")
# Subtract
difference = num1 - num2
print(f"Difference: {difference}")
# Product
product = num1 * num2
print(f"Product: {product}")
# Division
division = num1 / num2
print(f"Division: {division}")
#Floor Division
floor_div = num1 // num2
print(f"Floor Division: {floor_div}")
# Modulus
mod = num1 % num2
print(f"Modulus: {mod}")
#Power
pow = num1 ** num2
print(f"Power: {pow}")